export const ARTICLE_TOPIC_POOL: Record<string, string[]> = {
  horoscope: HOROSCOPE,
  tarot: TAROT,
  feng_shui: FENG_SHUI,
  lucky: LUCKY,
  general: GENERAL,
};

/** Pick a random topic from the given category pool (fallback: general). */
export function pickRandomTopic(category: string, seed?: number): string {
  const pool =
    ARTICLE_TOPIC_POOL[category] ?? ARTICLE_TOPIC_POOL.general;
  const idx =
    seed !== undefined
      ? Math.abs(seed) % pool.length
      : Math.floor(Math.random() * pool.length);
  return pool[idx];
}
